        <div class="col-xs-12">
            <strong>Fitting a Cooktop into Your Kitchen’s Design</strong>
            <p>
                Measure the space available for your new cooktop. Height, width and depth are crucial in deciding which
                unit is right for you. The cut-out is the primary dimension that is needed when selecting the correct
                model.
            </p>
        </div>
    </div>

    <div id="dimensions" class="col-xs-4">
        <img src="/assets/cms/cooktop-dimensions.jpg" class="img-responsive" alt="cooktop dimensions">
    </div>
    <div class="col-xs-8">
        <strong>Cooktop Dimensions</strong>
        <br>
        • <strong>Range of Widths:</strong> Cooktops are available in widths between 15" - 48"
        <br>
        • <strong>Range of Height:</strong> Cooktops are available in heights between 2" - 19.6"
        <br>
        • <strong>Range of Depths:</strong> Cooktops are available in depths between 21" - 22.25"
    </div>
    <div id="types" class="col-xs-12">
        <hr>
        <strong>Cooktop Types: Gas vs. Electric</strong>
        <p>
            Select the type of cooktop you would prefer. Cooktops are defined by the whether they use Gas or Electric to
            heat the elements.
        </p>
        <hr>
    </div>
    <div id="gas" class="col-xs-8">
        <strong>Gas Cooktops</strong>
        <br>
        Gas Cooktops use natural gas to heat the elements.
        <br><br>
        • <strong>Pros:</strong> A gas cooktop gets you an instant, constant flame that you can adjust visually and
        is therefore more intuitive. Flames cook food more quickly and have more even temperature control. The cost of
        setup and of using gas is less if your home already uses natural gas.
        <br>
        • <strong>Points to Consider:</strong> The cost of the cooktop is higher than that of an electric stove.
        Consider the cost involved to hook up a gas unit if you do not already have a gas line going to the kitchen.
        Keep in mind the potential for hazard if the burner does not ignite and gas is released into the air.
    </div>
    <div class="col-xs-4">
        <img src="/assets/cms/cooktop-gas.jpg" class="img-responsive" alt="gas cooktop">
    </div>
    <hr class="clear">
    <div class="col-xs-4">
        <img src="/assets/cms/cooktop-electric.jpg" class="img-responsive" alt="electric cooktop">
    </div>
    <div id="electric" class="col-xs-8">
        <strong>Electric Cooktops</strong>
        <br>
        Electric Cooktops use a 220 power outlet to supply power to the heating elements.
        <br><br>
        • <strong>Pros:</strong> An electric cooktop is sleek, made of glass and is therefore easy to wipe clean of
        most spills. There is no need to remove a burner ring or wipe around grates and gas nozzle and is also easy to
        operate and less expensive to purchase and install. Electric cooktops provide a table work surface for pots and
        pans and even distribution of heat around the pan.
        <br>
        • <strong>Points to Consider:</strong> With an electric cooktop, there is no steady temperature control
        (pulsing power) of the cooktop and food cooks more slowly than in gas ranges. Keep in mind that an electric
        cooktop is inoperable during power outages.
    </div>
    <hr class="clear">
    <div id="features" class="col-xs-12">
        <strong>Extra Features for Your Cooktop</strong>
        <br>
        Select the features that you need in your Cooktop. Cooktops have become quite innovative and many different
        features are available for each format.
        <br><br>
    </div>
    <div class="">
        <div class="col-xs-8">
            <u id="1">Cooking Surface</u>:
            <br>
            • Gas:
            <br>
            - Standard Open Burners – Require drip trays to catch spills
            <br>- Sealed Burners – The cooktop extends up to the burner so that spills cannot drip down the open space
            beneath the burner. Btu is slightly lower with sealed burners.
            <br>
            • Electric:
            <br>- Coil – Basic electric cooktop, takes time to heat up, may be difficult to clean.
            <br>- Radiant Glass Surface – Coils are housed under the surface of the smooth glass top. This radiates the
            heat up to your pot or pan. Any material of cookware (glass, copper-bottom, etc.) work fine.
            <br>- Induction Glass Surface – Compared to coil, induction surface adjusts heat instantly. Due to
            electromagnetic energy, heat is transferred directly to the cookware, while the surface of the smooth top
            stays cool to the touch. Plus, the glass surface is easy to clean. This is the most energy efficient
            cooktop. However, induction cookware is required for heating to take place.
            <br>
            <u id="2">Number of elements/burners</u>:
            <br>• 4 burners - Standard
            <br>• 5 burners - Designed to give you extra space and flexibility to cook more at once.
        </div>
        <div class="col-xs-4">
            <img src="/assets/cms/spin_prod_180941501.jpg" class="img-responsive" alt="washer features">
        </div>
        <div class="col-xs-12">
            <u id="3">Downdraft ventilation</u>
            : The option of having downdraft ventilation integrated with the product to pull heat and odors down and
            away from you cooktop. No need for an additional over the range microwave with ventilation or range hood.
            <br>
            <u id="4">Knob Control</u>:
            <br>• Side (Right/Left) – Standard on many freestanding ranges. There is the option for integrated backsplash
            for the controls.
            <br>• Front – Some freestanding ranges and all slide-in and drop-ins have the controls on the front. This
            allows for a sleek design without a necessary backsplash.
            <br>
            <u id="5">ADA compliant</u>
            : The Americans with Disabilities Act ensures that certain models are accessible for people with
            disabilities.
            <br>
            <u id="6">Installable Over Oven</u>
            : Helpful if you still want the oven and cooktop set together but with more customization.
            <br>
            <u id="7">Energy Star Compliant</u>
            : Reduces use of energy, with the added benefit of lowering your utility bill.
        </div>
